    private void UpdateMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_UpdateMouseClicked
        if(ProductID.getText().isEmpty()||ProductName.getText().isEmpty()||ProductQuantity.getText().isEmpty()||Brand.getText().isEmpty()){
            JOptionPane.showMessageDialog(this, "Missing Information");
        } else {
            try {
                String UpdateQuery = "UPDATE producttable SET ProductName='"+ ProductName.getText() +"' ,ProductQuantity='"+ ProductQuantity.getText()+"',Brand='"+Brand.getText()+"' WHERE ProductID='"+ProductID.getText()+"'";
                Statement Change = Con.createStatement();
                Change.executeUpdate(UpdateQuery);
                JOptionPane.showMessageDialog(this, "Product Updated Successfully");
                SelectProd();
            }catch (Exception e) {
                e.printStackTrace();
            }
        }            
    }//GEN-LAST:event_UpdateMouseClicked

    private void DeleteMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_DeleteMouseClicked
        if (ProductID.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Enter the Item to be Deleted");
        } else {
            try {
                String Id = ProductID.getText();
                String Query = "DELETE FROM producttable WHERE ProductID="+ Id;
                Statement Remove = Con.createStatement();
                Remove.executeUpdate(Query);
                SelectProd();
                JOptionPane.showMessageDialog(this, "Product Deleted Successfully");
            } catch(Exception e){
                e.printStackTrace();
            }
        }
    }//GEN-LAST:event_DeleteMouseClicked

