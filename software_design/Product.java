    private void AddMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_AddMouseClicked
            
            
        try{
            
            
                   
            java.sql.PreparedStatement add = Con.prepareStatement("insert into ProductTable values (?, ?, ?, ?)");
            
            add.setInt(1, Integer.parseInt(ProductID.getText()));
            add.setString(2, ProductName.getText());
            add.setInt(3, Integer.parseInt(ProductQuantity.getText()));
            add.setString(4, Brand.getText());
            int row = add.executeUpdate();
            JOptionPane.showMessageDialog(this, "Product Successfully Added");
            Con.close();
            SelectProd();
            
        }catch(Exception e) {
            e.printStackTrace();
        }    
    }//GEN-LAST:event_AddMouseClicked

