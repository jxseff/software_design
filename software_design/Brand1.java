    private void Update_BrandMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_Update_BrandMouseClicked
        if(BrandID.getText().isEmpty()||BrandName.getText().isEmpty()){
            JOptionPane.showMessageDialog(this, "Missing Information");
        } else {
            try {
                String UpdateQuery = "update BrandTable set BrandName='"+BrandName.getText()+"'where BrandID='"+BrandID.getText()+"'";
                Statement Change = Con.createStatement();
                Change.executeUpdate(UpdateQuery);
                JOptionPane.showMessageDialog(this, "Brand Updated Successfully");
                SelectProd();
            }catch (Exception e) {
                e.printStackTrace();
            }
        }
    }//GEN-LAST:event_Update_BrandMouseClicked

