    private void Delete_BrandMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_Delete_BrandMouseClicked
        if (BrandID.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Enter the Item to be Deleted");
        } else {
            try {
                String Id = BrandID.getText();
                String Query = "DELETE FROM brandtable WHERE BrandID="+ Id;
                Statement Remove = Con.createStatement();
                Remove.executeUpdate(Query);
                SelectProd();
                JOptionPane.showMessageDialog(this, "Brand Deleted Successfully");
            } catch(Exception e){
                e.printStackTrace();
            }
        }
    }//GEN-LAST:event_Delete_BrandMouseClicked

